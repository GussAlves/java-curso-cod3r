package colecoes;

public class Collection {

	/**
	 * Set
	 * 	- Não Ordenada (por Padrão)
	 *  - Não indexado
	 *  - Não aceita repetição
	 *  
	 * List 
	 * 	- Indexada
	 * 	- Aceita Repetição
	 *
	 * Map
	 * 	- Chave/Valor
	 * 	- Chave não aceita Repetição
	 *  - Valor aceita repetição
	 *  
	 * Queue
	 * 	- Implementa fila
	 *  - First in/First out (fifo)
	 *  
	 * Stack 
	 * 	- Implementa pilha (Stack)
	 *  - Last in / First out (lifo) 
	 */
}
